void ft_putnbr(int n)
{
  int tmp;

  if (n < 0)
  {
    write(1, '-', 1);
    n *= -1;
  }
  tmp = n;
  if (n > 9)
  {
    ft_putnbr(n / 10);
  }
  tmp = tmp % 10 + '0';
  write(1, &tmp, 1);
}
