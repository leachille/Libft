#include "libft.h"

int	ft_strequ(const char *s1, const char *s2)
{
	if (!(s1) || !(s2))
		return (0);
	if (!(*s1) && !(*s2))
		return (1);
	if (*s1 == *s2)
		return (ft_strequ(s1 + 1, s2 + 1));
	return (0);
}
